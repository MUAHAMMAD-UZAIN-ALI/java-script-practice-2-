// 12. Function to tell letter counts of a word
function letterCounts(word) {
    var counts = {};
    for (var i = 0; i < word.length; i++) {
        var letter = word[i];
        if (counts[letter] === undefined) {
            counts[letter] = 1;
        } else {
            counts[letter]++;
        }
    }
    return counts;
}

