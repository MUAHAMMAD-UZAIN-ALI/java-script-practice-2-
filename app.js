function calculateAge(dateOfBirth) {
    var dob = new Date(dateOfBirth);
    var ageDifMs = Date.now() - dob.getTime();
    var ageDate = new Date(ageDifMs);
    return Math.abs(ageDate.getUTCFullYear() - 1970);
}

